
void main(){
  ////// for ////////////////
    for (int i = 0; i < 5; i++) {
        print('GeeksForGeeks');
    }
  //////// for in///////////////
    var geeksForGeeks = [ 1, 2, 3, 4, 5 ];

    for (int i in geeksForGeeks) {
        print(i);
    }
  //////// while ///////////////
    var qeeksForGeeks = 4;
    int i = 1;
    while (i <= qeeksForGeeks) {
        print('Hello Geek');
        i++;
    }
    ////////// do while/////////////
    var ksForGeeks = 4;
    int a = 1;
    do {
        print('Hello Geek');
        a++;
    } while (a <= ksForGeeks);

}